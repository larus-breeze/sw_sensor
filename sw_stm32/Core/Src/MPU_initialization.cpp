//! Initialization of MPU, clock system, interfaces etc
extern "C" void MPU_initialization(void)
{
  SystemClock_Config();
  SystemCoreClockUpdate();

  // precise error vectors
  SCB->SHCSR |= (SCB_SHCSR_BUSFAULTENA_Msk | SCB_SHCSR_MEMFAULTENA_Msk | SCB_SHCSR_USGFAULTENA_Msk);

#if SET_FPU_FLUSH_TO_ZERO
  uint32_t fpscr = __get_FPSCR(); // set FPU flush to zero mode
  fpscr |= FPU_FPDSCR_FZ_Msk;	  // NANs will become 0.0f
  __set_FPSCR(fpscr);
#endif

#if ACTIVATE_FPU_EXCEPTION_TRAP
  // enable floating-point exception
  HAL_NVIC_SetPriority ((IRQn_Type) FPU_IRQn, EMERGENCY_ISR_PRIORITY, 0);
  HAL_NVIC_EnableIRQ ((IRQn_Type) FPU_IRQn);
#endif

#if 0 // test FPU exception
  volatile float test = 0.0f;
  test = 1.0f / test;
#endif

#if configUSE_TRACE_FACILITY == 1
	vTraceEnable(TRC_START);
#endif
  HAL_Init();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_I2C2_Init();
  MX_SDIO_SD_Init();
  MX_SPI1_Init();
//  MX_SPI2_Init();
//  MX_USART2_UART_Init();
  MX_USART6_UART_Init();
  MX_FATFS_Init();
  MX_ADC1_Init();

  UNIQUE_ID[1]=*(uint32_t *)0x1fff7a10;
  UNIQUE_ID[2]=*(uint32_t *)0x1fff7a14;
  UNIQUE_ID[3]=*(uint32_t *)0x1fff7a18;
  UNIQUE_ID[0]=UNIQUE_ID[1] ^ UNIQUE_ID[2] ^ UNIQUE_ID[3];

#if WRITE_EEPROM_DEFAULTS
  void write_EEPROM_defaults( void);
  EEPROM_initialize();
  write_EEPROM_defaults();
#endif

}

